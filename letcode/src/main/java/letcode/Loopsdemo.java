package letcode;

public class Loopsdemo {

	public static void main(String[] args) {
		for (int i=0;i<=5;i++)
			System.out.print(i);
		
				System.out.println("---------------------------");
		
       int i=1;   //initialization
       while(i<5) {                //condition
    	    	   System.out.print(i);
    	   i++;                          //increment/decrement
       }
	System.out.println("***********************");
	
	
	
	
	
	}

}
